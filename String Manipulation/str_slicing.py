""" String Slicing
slicing means peace of string from start to stop access by string slicing
syntax:
string[start:end:step]
start and step are optional 
"""


string='python is My Base Language'
#string slicing with positive
print(string[:])
print(string[1:])# ignore the first charcter IF STRING OR word if list
print(string[:2])#first two char
print(string[1:3])#middle two char


#negative indexing


print(string[-3:-1])#print the middle two from last #ag
print(string[-3:])#age 



